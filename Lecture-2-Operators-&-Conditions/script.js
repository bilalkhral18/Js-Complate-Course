// Unary Operators

let a = 10;
let b = 20;
// post increment : first use the value then increment it
console.log(a++); // 10
console.log(a); // 11
// pre increment : first increment the value then use it
console.log(++b); // 21
// post decrement : first use the value then decrement it
console.log(b--); // 21
console.log(b); // 20
// pre decrement : first decrement the value then use it
console.log(--b); // 19
